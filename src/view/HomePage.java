    public void loadCars() {
        // load the cars list inside a table in "cars" section of the page
        DefaultTableModel tableModel = (DefaultTableModel) jTable2.getModel();
        tableModel.setRowCount(0);
        
        for(Car c : cars) {
            Object[] row = {
                c.getCarId(),
                c.getCarBrand(),
                c.getCarColor(),
                c.getRentPerDay(),
                c.getIsAvailable()
            };
            tableModel.addRow(row);
        }
        
    }
    
    public void updateCarsQuantity() {
        // display total cars, available cars and rented cars in the home page
        jLabel5.setText("Total Cars â›Ÿ:"+cars.size());
        int availableCount = 0;
        int rentedCount = 0;
        for(Car each:cars){
            if(each.getIsAvailable() == true) availableCount++; // if available status is true, increase the available count
            else rentedCount++;// if available status is false, increase the available count
        }
        jLabel8.setText("Available Cars âœ…:"+availableCount);
        jLabel9.setText("Rented Cars ðŸš«:"+rentedCount);
    }
    
