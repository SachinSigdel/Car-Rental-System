    public void loadStudentListToTable() {
        DefaultTableModel tableModel = (DefaultTableModel) jTable2.getModel();
        tableModel.setRowCount(0);
        
        for(Car c : cars) {
            Object[] row = {
                c.getCarId(),
                c.getCarBrand(),
                c.getCarColor(),
                c.getRentPerDay(),
                c.getIsAvailable()
            };
            tableModel.addRow(row);
        }
        
    }
    
    public void updateCarsQuantity() {
        jLabel5.setText("Total Cars â›Ÿ:"+cars.size());
        int availableCount = 0;
        int rentedCount = 0;
        for(Car each:cars){
            if(each.getIsAvailable() == true) availableCount++;
            else rentedCount++;
        }
        jLabel8.setText("Available Cars âœ…:"+availableCount);
        jLabel9.setText("Rented Cars ðŸš«:"+rentedCount);
    }
    
